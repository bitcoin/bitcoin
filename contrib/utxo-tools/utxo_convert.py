"""Tool to convert a compact-serialized UTXO set to an SQLite3 database or CSV.

The input UTXO set can be generated by Bitcoin Core with the `dumptxoutset` RPC:
$ bitcoin-cli dumptxoutset ~/utxos.dat latest

For SQLite output (--format=sqlite):
* The created database contains a table `utxos` with the following schema:
  (txid TEXT, vout INT, value INT, coinbase INT, height INT, scriptpubkey TEXT)
* If --txid=raw or --txid=rawle is specified, txid will be BLOB instead;
* if --spk=raw, then scriptpubkey will be BLOB instead.

For CSV output (--format=csv):
* The created output contains fields (txid, vout, value, coinbase, height, descriptor).
"""
import argparse
import csv
import hashlib
import os
import sqlite3
import sys
import time
from dataclasses import dataclass
from enum import Enum
from typing import BinaryIO, Iterator

# Constants

UTXO_DUMP_MAGIC = b'utxo\xff'
UTXO_DUMP_VERSION = 2
NET_MAGIC_BYTES = {
    b"\xf9\xbe\xb4\xd9": "Mainnet",
    b"\x0a\x03\xcf\x40": "Signet",
    b"\x0b\x11\x09\x07": "Testnet3",
    b"\x1c\x16\x3f\x28": "Testnet4",
    b"\xfa\xbf\xb5\xda": "Regtest",
}

P2PKH_VERSIONS = {
    "Mainnet": 0,
    "Testnet3": 111,
    "Testnet4": 111,
    "Signet": 111,
    "Regtest": 111,
}

P2SH_VERSIONS = {
    "Mainnet": 5,
    "Testnet3": 196,
    "Testnet4": 196,
    "Signet": 196,
    "Regtest": 196,
}

HRP_VERSIONS = {
    "Mainnet": "bc",
    "Testnet3": "tb",
    "Testnet4": "tb",
    "Signet": "tb",
    "Regtest": "bcrt",
}

# Base58 encoding logic

BASE58_CHARS = '123456789ABCDEFGHJKLMNPQRSTUVWXYZabcdefghijkmnopqrstuvwxyz'


def byte_to_base58(b: bytes, version: int) -> str:
    """Compute the Base58Check encoding of an input byte array with given version."""
    result = ''
    b = bytes([version]) + b  # prepend version
    b += hashlib.sha256(hashlib.sha256(b).digest()).digest()[:4]  # append checksum
    value = int.from_bytes(b, 'big')
    while value > 0:
        result = BASE58_CHARS[value % 58] + result
        value //= 58
    while b[0] == 0:
        result = BASE58_CHARS[0] + result
        b = b[1:]
    return result


# Bech32 encoding logic

BECH32_CHARSET = "qpzry9x8gf2tvdw0s3jn54khce6mua7l"
BECH32_CONST = 1
BECH32M_CONST = 0x2bc830a3
BECH32_GENERATOR = [0x3b6a57b2, 0x26508e6d, 0x1ea119fa, 0x3d4233dd, 0x2a1462b3]


class Encoding(Enum):
    """Enumeration type to list the various supported encodings."""
    BECH32 = 1
    BECH32M = 2


def bech32_polymod(values: list[int]) -> int:
    """Internal function that computes the Bech32 checksum."""
    chk = 1
    for value in values:
        top = chk >> 25
        chk = (chk & 0x1ffffff) << 5 ^ value
        for i in range(5):
            chk ^= BECH32_GENERATOR[i] if ((top >> i) & 1) else 0
    return chk


def bech32_hrp_expand(hrp: str) -> list[int]:
    """Expand the HRP into values for checksum computation."""
    return [ord(x) >> 5 for x in hrp] + [0] + [ord(x) & 31 for x in hrp]


def bech32_create_checksum(encoding: Encoding, hrp: str, data: list[int]) -> list[int]:
    """Compute the checksum values given HRP and data."""
    values = bech32_hrp_expand(hrp) + data
    const = BECH32M_CONST if encoding == Encoding.BECH32M else BECH32_CONST
    polymod = bech32_polymod(values + [0, 0, 0, 0, 0, 0]) ^ const
    return [(polymod >> 5 * (5 - i)) & 31 for i in range(6)]


def bech32_encode(encoding: Encoding, hrp: str, data: list[int]) -> str:
    """Compute a Bech32 or Bech32m string given HRP and data values."""
    combined = data + bech32_create_checksum(encoding, hrp, data)
    return hrp + '1' + ''.join([BECH32_CHARSET[d] for d in combined])


def convertbits(data: bytes | list[int], frombits: int, tobits: int, pad: bool = True) -> list[int] | None:
    """General power-of-2 base conversion."""
    acc = 0
    bits = 0
    ret = []
    maxv = (1 << tobits) - 1
    max_acc = (1 << (frombits + tobits - 1)) - 1
    for value in data:
        if value < 0 or (value >> frombits):
            return None
        acc = ((acc << frombits) | value) & max_acc
        bits += frombits
        while bits >= tobits:
            bits -= tobits
            ret.append((acc >> bits) & maxv)
    if pad:
        if bits:
            ret.append((acc << (tobits - bits)) & maxv)
    elif bits >= frombits or ((acc << (tobits - bits)) & maxv):
        return None
    return ret


def encode_segwit_address(hrp: str, witver: int, witprog: bytes) -> str:
    """Encode a segwit address."""
    encoding = Encoding.BECH32 if witver == 0 else Encoding.BECH32M
    symbols = convertbits(witprog, frombits=8, tobits=5)
    assert symbols is not None
    return bech32_encode(encoding, hrp, [witver] + symbols)

# Address/descriptor encoding


# Opcode constants.
OP_0 = 0x00
OP_1 = 0x51
OP_16 = 0x60
OP_NOP = 0x61
OP_DUP = 0x76
OP_EQUAL = 0x87
OP_EQUALVERIFY = 0x88
OP_HASH160 = 0xa9
OP_CHECKSIG = 0xac
OP_CHECKMULTISIG = 0xae
OP_CHECKSIGADD = 0xba


def parse_opcode(spk: bytes, pos: int) -> tuple[int | None, bytes | None, int]:
    """Parse an opcode or byte push from spk at byte position pos."""
    # End of script.
    if pos >= len(spk):
        return None, None, pos
    # Direct push.
    if spk[pos] <= 0x49:
        push_len = spk[pos]
        # Detect straddling of script end.
        if pos + 1 + push_len > len(spk):
            return None, None, pos
        return spk[pos], spk[pos + 1:pos + 1 + push_len], pos + 1 + push_len
    # Normal opcodes (including OP_n).
    if spk[pos] >= OP_1 and spk[pos] <= OP_CHECKSIGADD:
        return spk[pos], None, pos + 1
    # Some opcodes (OP_RESERVED, OP_PUSHDATAn, undefined opcodes) are not supported here.
    return None, None, pos


def decode_small_int(opcode: int | None, data: bytes | None) -> int | None:
    """Decode a small integer from parse_opcode result (direct push or OP_n)."""
    # Invalid script.
    if opcode is None:
        return None
    # Special case for OP_0.
    if opcode == OP_0:
        return 0
    # Special case for OP_1 through OP_16.
    if OP_1 <= opcode <= OP_16:
        return opcode - OP_1 + 1
    # Small integer direct push (only if minimal).
    if data is not None and len(data) == 1 and data[0] > 16:
        return data[0]
    # Not a small integer opcode/push.
    return None


def is_valid_pubkey(key: bytes) -> bool:
    """Determine whether key is a valid public key (excluding on-curve check)."""
    # Compressed and hybrid keys.
    if key is not None and len(key) == 33 and (key[0] & ~0x05) == 0x02:
        return True
    # Uncompressed keys.
    if key is not None and len(key) == 65 and key[0] == 0x04:
        return True
    return False


def parse_p2pk(spk: bytes) -> bytes | None:
    """Given a scriptpubkey, return key if P2PK, None otherwise."""
    # Quick check to prevent wasting time on obviously non-P2PK scripts.
    if len(spk) == 0 or spk[-1] != OP_CHECKSIG:
        return None
    opcode, key, pos = parse_opcode(spk, 0)
    if key is None or not is_valid_pubkey(key):
        return None
    opcode, _, pos = parse_opcode(spk, pos)
    if opcode != OP_CHECKSIG:
        return None
    if pos != len(spk):
        return None
    return key


def parse_p2ms(spk: bytes) -> tuple[list[bytes], int] | None:
    """Given a scriptpubkey, return ([keys],m) if bare multisig, None otherwise."""
    # Quick check to prevent wasting time on obviously non-multisig scripts.
    if len(spk) == 0 or spk[-1] != OP_CHECKMULTISIG:
        return None
    keys = []
    # Decode m.
    opcode, data, pos = parse_opcode(spk, 0)
    m = decode_small_int(opcode, data)
    if m is None or m > 20:
        return None
    # Decode public keys.
    while True:
        opcode, data, pos = parse_opcode(spk, pos)
        if data is not None and is_valid_pubkey(data):
            keys.append(data)
            continue
        break
    # Decode n.
    n = decode_small_int(opcode, data)
    if n is None or n < m or n > 20 or n != len(keys):
        return None
    # Decode OP_CHECKMULTISIG
    opcode, data, pos = parse_opcode(spk, pos)
    if opcode is None or opcode != OP_CHECKMULTISIG:
        return None
    # Finish
    if pos != len(spk):
        return None
    return keys, m


def scriptpubkey_to_address(spk: bytes, network_string: str) -> str | None:
    """Infer an address for the specified scriptpubkey, if possible."""
    # P2PKH
    if (len(spk) == 25 and spk[0] == OP_DUP and spk[1] == OP_HASH160 and spk[2] == 20
            and spk[-2] == OP_EQUALVERIFY and spk[-1] == OP_CHECKSIG):
        return byte_to_base58(spk[3:23], P2PKH_VERSIONS[network_string])
    # P2SH
    if len(spk) == 23 and spk[0] == OP_HASH160 and spk[1] == 20 and spk[-1] == OP_EQUAL:
        return byte_to_base58(spk[2:22], P2SH_VERSIONS[network_string])
    # P2WPKH
    if len(spk) == 22 and spk[0] == OP_0 and spk[1] == 20:
        return encode_segwit_address(HRP_VERSIONS[network_string], 0, spk[2:])
    # P2WSH
    if len(spk) == 34 and spk[0] == OP_0 and spk[1] == 32:
        return encode_segwit_address(HRP_VERSIONS[network_string], 0, spk[2:])
    # Other native witness outputs (incl. P2TR).
    if (len(spk) >= 4 and len(spk) <= 42 and spk[0] >= OP_1 and spk[0] <= OP_16
            and spk[1] + 2 == len(spk)):
        return encode_segwit_address(HRP_VERSIONS[network_string], spk[0] - OP_1 + 1, spk[2:])
    # No well-defined address.
    return None


def scriptpubkey_to_descriptor(spk: bytes, network_string: str) -> str:
    """Infer a descriptor for the specified scriptpubkey."""
    # addr(ADDR)
    addr = scriptpubkey_to_address(spk, network_string)
    if addr is not None:
        return f"addr({addr})"
    # pk(KEY)
    key = parse_p2pk(spk)
    if key is not None:
        return f"pk({key.hex()})"
    # multi(k,KEY,KEY,...)
    multi = parse_p2ms(spk)
    if multi is not None:
        keys, m = multi
        return "multi(" + ",".join([str(m)] + [key.hex() for key in keys]) + ")"
    # raw(HEX)
    return f"raw({spk.hex()})"


def read_varint(f: BinaryIO) -> int:
    """Equivalent of `ReadVarInt()` (see serialization module)."""
    n = 0
    while True:
        dat = f.read(1)[0]
        n = (n << 7) | (dat & 0x7f)
        if (dat & 0x80) > 0:
            n += 1
        else:
            return n


def read_compactsize(f: BinaryIO) -> int:
    """Equivalent of `ReadCompactSize()` (see serialization module)."""
    n = f.read(1)[0]
    if n == 253:
        n = int.from_bytes(f.read(2), "little")
    elif n == 254:
        n = int.from_bytes(f.read(4), "little")
    elif n == 255:
        n = int.from_bytes(f.read(8), "little")
    return n


def decompress_amount(x: int) -> int:
    """Equivalent of `DecompressAmount()` (see compressor module)."""
    if x == 0:
        return 0
    x -= 1
    e = x % 10
    x //= 10
    n = 0
    if e < 9:
        d = (x % 9) + 1
        x //= 9
        n = x * 10 + d
    else:
        n = x + 1
    while e > 0:
        n *= 10
        e -= 1
    return n


def decompress_script(f: BinaryIO) -> bytes:
    """Equivalent of `DecompressScript()` (see compressor module)."""
    size = read_varint(f)  # sizes 0-5 encode compressed script types
    if size == 0:  # P2PKH
        return bytes([0x76, 0xa9, 20]) + f.read(20) + bytes([0x88, 0xac])
    if size == 1:  # P2SH
        return bytes([0xa9, 20]) + f.read(20) + bytes([0x87])
    if size in (2, 3):  # P2PK (compressed)
        return bytes([33, size]) + f.read(32) + bytes([0xac])
    if size in (4, 5):  # P2PK (uncompressed)
        compressed_pubkey = bytes([size - 2]) + f.read(32)
        return bytes([65]) + decompress_pubkey(compressed_pubkey) + bytes([0xac])
    # others (bare multisig, segwit etc.)
    size -= 6
    assert size <= 10000, f"too long script with size {size}"
    return f.read(size)


def decompress_pubkey(compressed_pubkey: bytes) -> bytes:
    """Decompress pubkey by calculating y = sqrt(x^3 + 7) % p
       (see functions `secp256k1_eckey_pubkey_parse` and `secp256k1_ge_set_xo_var`).
    """
    P = 2**256 - 2**32 - 977  # secp256k1 field size
    assert len(compressed_pubkey) == 33 and compressed_pubkey[0] in (2, 3)
    x = int.from_bytes(compressed_pubkey[1:], 'big')
    rhs = (x**3 + 7) % P
    y = pow(rhs, (P + 1) // 4, P)  # get sqrt using Tonelli-Shanks algorithm (for p % 4 = 3)
    assert pow(y, 2, P) == rhs, f"pubkey is not on curve ({compressed_pubkey.hex()})"
    tag_is_odd = compressed_pubkey[0] == 3
    y_is_odd = (y & 1) == 1
    if tag_is_odd != y_is_odd:  # fix parity (even/odd) if necessary
        y = P - y
    return bytes([4]) + x.to_bytes(32, 'big') + y.to_bytes(32, 'big')


@dataclass(slots=True)
class UTXORecord:
    """Data type for internal representation of UTXOs."""
    coin_idx: int
    prevout_hash: bytes
    prevout_index: int
    height: int
    is_coinbase: bool
    amount: int
    scriptpubkey: bytes


class UTXOReader:
    """Class to decode Bitcoin Core UTXO dump files."""

    def __init__(self, filename: str, verbose: bool):
        """Construct a new UTXOReader for the given file, which is not yet opened."""
        self._filename = filename
        self._file: BinaryIO | None = None
        self._num_utxos: int | None = None
        self._network_string: str | None = None
        self._max_height = 0
        self._verbose = verbose

    def __enter__(self) -> "UTXOReader":
        """Open the file and parse the header."""
        self._file = open(self._filename, 'rb')
        # read metadata (magic bytes, version, network magic, block hash, UTXO count)
        magic_bytes = self._file.read(5)
        version = int.from_bytes(self._file.read(2), 'little')
        network_magic = self._file.read(4)
        block_hash = self._file.read(32)
        self._num_utxos = int.from_bytes(self._file.read(8), 'little')
        if magic_bytes != UTXO_DUMP_MAGIC:
            print(f"Error: provided input file '{self._filename}' is not a UTXO dump.")
            sys.exit(1)
        if version != UTXO_DUMP_VERSION:
            print(f"Error: provided input file '{self._filename}' has unknown UTXO dump version {version} "
                  f"(only version {UTXO_DUMP_VERSION} supported)")
            sys.exit(1)
        self._network_string = NET_MAGIC_BYTES.get(network_magic, f"unknown network ({network_magic.hex()})")
        print(f"UTXO Snapshot for {self._network_string} at block hash "
              f"{block_hash[::-1].hex()[:32]}..., contains {self._num_utxos} coins")
        return self

    def __exit__(self, _exc_type: str, _exc_value: str, _traceback: str) -> None:
        """Close the file."""
        if self._file:
            self._file.close()

    @property
    def num_utxos(self) -> int:
        """Get the total number of UTXOs in this dump."""
        assert self._num_utxos is not None
        return self._num_utxos

    @property
    def max_height(self) -> int:
        """Get the maximum height of a record returned by records so far."""
        return self._max_height

    @property
    def network_string(self) -> str:
        """Get the network identifier string."""
        assert self._network_string is not None
        return self._network_string

    @property
    def records(self) -> Iterator[UTXORecord]:
        """Get a generator for all the UTXO records in the dump."""
        assert self._file is not None
        coins_per_hash_left = 0
        prevout_hash = None
        prevout_index = 0
        start_time = time.time()
        for coin_idx in range(1, self.num_utxos + 1):
            # read key (COutPoint)
            if coins_per_hash_left == 0:  # read next prevout hash
                prevout_hash = self._file.read(32)
                coins_per_hash_left = read_compactsize(self._file)
            prevout_index = read_compactsize(self._file)
            # read value (Coin)
            code = read_varint(self._file)
            height = code >> 1
            is_coinbase = bool(code & 1)
            amount = decompress_amount(read_varint(self._file))
            scriptpubkey = decompress_script(self._file)
            # Convert.
            assert prevout_hash is not None
            yield UTXORecord(coin_idx, prevout_hash, prevout_index, height, is_coinbase, amount, scriptpubkey)
            coins_per_hash_left -= 1
            self._max_height = max(height, self._max_height)
            # Report.
            if self._verbose:
                print(f"Coin {coin_idx}/{self.num_utxos}:")
                print(f"    prevout = {prevout_hash[::-1].hex()}:{prevout_index}")
                print(f"    amount = {amount}, height = {height}, coinbase = {is_coinbase}")
                print(f"    scriptPubKey = {scriptpubkey.hex()}\n")
            if coin_idx % (1024 * 1024) == 0:
                elapsed = time.time() - start_time
                print(f"{coin_idx} coins converted [{coin_idx / self.num_utxos * 100:.2f}%], "
                      f"{elapsed:.3f}s passed since start")
        if self._file.read(1) != b'':  # EOF should be reached by now
            print(f"WARNING: input file {self._filename} has not reached EOF yet!")
            sys.exit(1)


def convert_to_sqlite(reader: UTXOReader,
                      filename: str,
                      spk_hex: bool,
                      txid_hex: bool,
                      txid_reverse: bool) -> None:
    """Convert from the specified UTXO reader to SQLite."""
    txid_fmt = "TEXT" if txid_hex else "BLOB"
    spk_fmt = "TEXT" if spk_hex else "BLOB"
    with sqlite3.connect(filename) as con:
        # create database table
        con.execute(f"CREATE TABLE utxos(txid {txid_fmt}, vout INT, value INT, coinbase INT, height INT, scriptpubkey {spk_fmt})")
        write_batch = []
        num_utxos = reader.num_utxos
        for record in reader.records:
            scriptpubkey_write = record.scriptpubkey.hex() if spk_hex else record.scriptpubkey
            txid = record.prevout_hash
            if txid_reverse:
                txid = txid[::-1]
            txid_write: bytes | str
            if txid_hex:
                txid_write = txid.hex()
            else:
                txid_write = txid
            write_batch.append((txid_write, record.prevout_index, record.amount, record.is_coinbase, record.height, scriptpubkey_write))

            if record.coin_idx % (16 * 1024) == 0 or record.coin_idx == num_utxos:
                # write utxo batch to database
                con.executemany("INSERT INTO utxos VALUES(?, ?, ?, ?, ?, ?)", write_batch)
                con.commit()
                write_batch.clear()


def convert_to_csv(reader: UTXOReader, filename: str) -> None:
    """Convert from the specified UTXO reader to CSV."""
    network_string = reader.network_string
    with open(filename, 'w', encoding='utf-8', newline='') as csvfile:
        writer = csv.DictWriter(csvfile, ['txid', 'vout', 'value', 'coinbase', 'height', 'descriptor'])
        writer.writeheader()
        for record in reader.records:
            csv_record = {
                "txid": record.prevout_hash[::-1].hex(),
                "vout": record.prevout_index,
                "value": record.amount,
                "coinbase": int(record.is_coinbase),
                "height": record.height,
                "descriptor": scriptpubkey_to_descriptor(record.scriptpubkey, network_string)
            }
            writer.writerow(csv_record)


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument('infile', help='filename of compact-serialized UTXO set (input)')
    parser.add_argument('outfile', help='filename of created SQLite3 database (output)')
    parser.add_argument('--verbose', action='store_true', help='show details about each UTXO')
    parser.add_argument('--format', choices=['auto', 'sqlite', 'csv'], default='auto', help='Select output format (auto autodetects based on filename extension)')
    parser.add_argument('--spk', choices=['hex', 'raw'], default='hex', help='encode scriptPubKey as hex or raw bytes. Only has effect for sqlite output format.')
    parser.add_argument('--txid', choices=['hex', 'raw', 'rawle'], default='hex', help='encode txid as hex, raw bytes (sha256 byteorder), or reversed raw bytes (little endian). Only has effect for sqlite output format.')
    args = parser.parse_args()

    if not os.path.exists(args.infile):
        print(f"Error: provided input file '{args.infile}' doesn't exist.")
        sys.exit(1)

    if os.path.exists(args.outfile):
        print(f"Error: provided output file '{args.outfile}' already exists.")
        sys.exit(1)

    # Auto detect output format.
    output_format = args.format
    if output_format == 'auto':
        if args.outfile.endswith('.csv') or args.outfile.endswith('.txt'):
            output_format = 'csv'
        elif args.outfile.endswith('.sqlite') or args.outfile.endswith('.dat'):
            output_format = 'sqlite'
        else:
            print(f"Error: ambiguous output file format for '{args.outfile}. Use --format.")
            sys.exit(1)

    # Convert.
    with UTXOReader(args.infile, args.verbose) as reader:
        if output_format == 'sqlite':
            spk_hex = args.spk == 'hex'
            txid_hex = args.txid == 'hex'
            txid_reverse = args.txid != 'raw'
            convert_to_sqlite(reader, args.outfile, spk_hex, txid_hex, txid_reverse)
        elif output_format == 'csv':
            convert_to_csv(reader, args.outfile)
        else:
            print(f"Error: unknown output file format '{format}.")
            sys.exit(1)
        print(f"TOTAL: {reader.num_utxos} coins written to {args.outfile}, snapshot height is {reader.max_height}.")


if __name__ == '__main__':
    main()
